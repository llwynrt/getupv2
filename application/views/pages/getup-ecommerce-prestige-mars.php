<h2>Détails de l'évènement</h2>
<p>
Les tarifs indiqués sont TTC : entreprises, vous récupérez la TVA.</p>
 
<p class="important">Vous vous apprêtez à lancer votre entreprise et vous songez à la doter d'un site ecommerce.
En seulement une semaine, accompagné par des professionnels du web, repartez avec un site à votre image.</p>

<p>Ce que j'obtiens :</p>

    <ul><li><strong>avec un professionnel du lean startup : l'agilité d'entreprise.</strong><br>
    Je découvre comment dépenser moins pour tester rapidement mon <strong>business model</strong> sans me mettre en danger financièrement.<br>
    Je gagne du temps.<br>
    Je réalise avec le groupe mon <strong>business model canvas</strong>.</li>
    <li><strong>avec un professionnel des projets web : mon cahier des charges et mon site ecommerce sous Prestashop.</strong><br>
    Comment exprimer mes besoins et, grâce aux <strong>méthodes agiles</strong>, comment éviter les principaux écueils qui font échouer les projets.<br>
    Je réalise mon cahier des charges avec des outils simples.<br><br>

    Il m'apprend à utiliser le backoffice du système de gestion de contenu et <strong>comment mettre mon site en ligne</strong>. 
	Je découvre les bases du développement internet avec les langages <strong>HTML et CSS</strong>. 
	Je suis capable de faire évoluer le contenu et l'apparence de mon site.<br>
    Je connais les bases du référencement internet et de la rédaction de <strong>contenu optimisé pour Google</strong>.</li>
    <li><strong>avec un professionnel du graphisme : une charte graphique adaptée à mes besoins</strong>.<br>
    Le web designer réalise ma <strong>charte graphique incluant un logo simple</strong>, mes couleurs et mes typographies.<br>
    Il m'apprend les règles de base de la communication graphique, les erreurs à éviter.</li></ul>

<p class="important">Résultat : mon site ecommerce simple est en ligne.
Le prix inclut le nom de domaine et l'hébergement de mon site pour un an. Je n'ai rien d'autre à payer.</p>


<p>Je suis autonome : je sais ajouter du contenu et réaliser des modifications simples.
En cas de besoin, je peux faire appel à des professionnels parmi les meilleurs du secteur pour m'aider à perfectionner mon site.</p>

<p>J'ai appris les bases du code informatique. Je comprends les enjeux de la création de site internet et du développement de mon entreprise.</p>

<p>Parce que j'ai trouvé rassemblés en un même lieu et un même temps, toutes les compétences nécessaires à la réussite de mon projet web.<br>
Parce que j'ai découvert et mis en oeuvre une <strong>méthode agile (Scrum)</strong>.<br>
Et surtout : du concret, rapidement.<br>
J'ai gagné un temps précieux. Et ça, ça n'a pas de prix. </p>
